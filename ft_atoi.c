#include "libft.h"

int	ft_atoi(const char *str)
{
	int	num;
	int	i;
	int	count;

	num = 0;
	i = 0;
	count = 1;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+')
		i++;
	if (str[i] == '-')
	{
		count = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num *= 10;
		num += str[i] - 48;
		i++;
	}
	num *= count;
	return (num);
}
